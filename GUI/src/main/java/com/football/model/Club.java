package com.football.model;

public class Club {
    private String clubId;
    private String clubCode;
    private String name;
    private String domesticCompetitionId;
    private String squadSize;
    private String averageAge;
    private String foreignersNumber;
    private String stadiumName;
    private String stadiumSeats;
    private String coachName;
    private String lastSeason;
    private String totalMarketValue;
    private String url;

    public Club() {}

    public String getClubId() { return clubId; }
    public void setClubId(String clubId) { this.clubId = clubId; }
    public String getClubCode() { return clubCode; }
    public void setClubCode(String clubCode) { this.clubCode = clubCode; }
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    public String getDomesticCompetitionId() { return domesticCompetitionId; }
    public void setDomesticCompetitionId(String d) { this.domesticCompetitionId = d; }
    public String getSquadSize() { return squadSize; }
    public void setSquadSize(String squadSize) { this.squadSize = squadSize; }
    public String getAverageAge() { return averageAge; }
    public void setAverageAge(String averageAge) { this.averageAge = averageAge; }
    public String getForeignersNumber() { return foreignersNumber; }
    public void setForeignersNumber(String foreignersNumber) { this.foreignersNumber = foreignersNumber; }
    public String getStadiumName() { return stadiumName; }
    public void setStadiumName(String stadiumName) { this.stadiumName = stadiumName; }
    public String getStadiumSeats() { return stadiumSeats; }
    public void setStadiumSeats(String stadiumSeats) { this.stadiumSeats = stadiumSeats; }
    public String getCoachName() { return coachName; }
    public void setCoachName(String coachName) { this.coachName = coachName; }
    public String getLastSeason() { return lastSeason; }
    public void setLastSeason(String lastSeason) { this.lastSeason = lastSeason; }
    public String getTotalMarketValue() { return totalMarketValue; }
    public void setTotalMarketValue(String totalMarketValue) { this.totalMarketValue = totalMarketValue; }
    public String getUrl() { return url; }
    public void setUrl(String url) { this.url = url; }
}

package com.football.model;

public class Player {
    private String playerId;
    private String firstName;
    private String lastName;
    private String name;
    private String lastSeason;
    private String currentClubId;
    private String countryOfBirth;
    private String countryOfCitizenship;
    private String dateOfBirth;
    private String subPosition;
    private String position;
    private String foot;
    private String heightInCm;
    private String imageUrl;
    private String currentClubName;
    private String marketValueInEur;
    private String highestMarketValueInEur;
    private String url;

    public Player() {}

    public String getPlayerId() { return playerId; }
    public void setPlayerId(String v) { this.playerId = v; }
    public String getFirstName() { return firstName; }
    public void setFirstName(String v) { this.firstName = v; }
    public String getLastName() { return lastName; }
    public void setLastName(String v) { this.lastName = v; }
    public String getName() { return name; }
    public void setName(String v) { this.name = v; }
    public String getLastSeason() { return lastSeason; }
    public void setLastSeason(String v) { this.lastSeason = v; }
    public String getCurrentClubId() { return currentClubId; }
    public void setCurrentClubId(String v) { this.currentClubId = v; }
    public String getCountryOfBirth() { return countryOfBirth; }
    public void setCountryOfBirth(String v) { this.countryOfBirth = v; }
    public String getCountryOfCitizenship() { return countryOfCitizenship; }
    public void setCountryOfCitizenship(String v) { this.countryOfCitizenship = v; }
    public String getDateOfBirth() { return dateOfBirth; }
    public void setDateOfBirth(String v) { this.dateOfBirth = v; }
    public String getSubPosition() { return subPosition; }
    public void setSubPosition(String v) { this.subPosition = v; }
    public String getPosition() { return position; }
    public void setPosition(String v) { this.position = v; }
    public String getFoot() { return foot; }
    public void setFoot(String v) { this.foot = v; }
    public String getHeightInCm() { return heightInCm; }
    public void setHeightInCm(String v) { this.heightInCm = v; }
    public String getImageUrl() { return imageUrl; }
    public void setImageUrl(String v) { this.imageUrl = v; }
    public String getCurrentClubName() { return currentClubName; }
    public void setCurrentClubName(String v) { this.currentClubName = v; }
    public String getMarketValueInEur() { return marketValueInEur; }
    public void setMarketValueInEur(String v) { this.marketValueInEur = v; }
    public String getHighestMarketValueInEur() { return highestMarketValueInEur; }
    public void setHighestMarketValueInEur(String v) { this.highestMarketValueInEur = v; }
    public String getUrl() { return url; }
    public void setUrl(String v) { this.url = v; }
}
